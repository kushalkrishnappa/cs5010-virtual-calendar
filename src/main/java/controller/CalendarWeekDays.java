package controller;

/**
 * The WeekDay class used with the {@link EventData} class.
 *
 * <p>The enum constants represent the days of the week as follows:
 * <ul>
 * <li>{@code M}: Monday</li>
 * <li>{@code T}: Tuesday</li>
 * <li>{@code W}: Wednesday</li>
 * <li>{@code R}: Thursday</li>
 * <li>{@code F}: Friday</li>
 * <li>{@code S}: Saturday</li>
 * <li>{@code U}: Sunday</li>
 * </ul>
 */
public enum CalendarWeekDays {

  /**
   * Monday.
   */
  M,

  /**
   * Tuesday.
   */
  T,

  /**
   * Wednesday.
   */
  W,

  /**
   * Thursday.
   */
  R,

  /**
   * Friday.
   */
  F,

  /**
   * Saturday.
   */
  S,

  /**
   * Sunday.
   */
  U;
}
